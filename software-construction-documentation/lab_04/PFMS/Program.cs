const string DataPath = "data/pfms.json";

// ── 1. Завантаження / ініціалізація бази даних ────────────────────────────
// AppDatabase.Load() повертає існуючі дані або демо-набір (seed data),
// що відтворює сценарії з діаграм об'єктів Лабораторної роботи №1.
var db = AppDatabase.Load(DataPath);

// ── 2. Створення сервісів ─────────────────────────────────────────────────
// Порядок важливий: AuditService не залежить від інших,
// тому він ініціалізується першим і передається в решту.
var auditService    = new AuditService(db);
var employeeService = new EmployeeService(db, auditService);
var fileService     = new PersonalFileService(db, auditService);

// ── 3. Запуск UI ─────────────────────────────────────────────────────────
var menu = new ConsoleMenu(db, employeeService, fileService, auditService);
menu.Run();
